// Описание:
//     Дан массив (или список) оценок, вернуть массив рангов для каждого значения в массиве. Наибольшее значение имеет ранг 1, второе — ранг 2 и т. д.
//     Равные значения следует обрабатывать, присваивая одинаковый ранг всем равным значениям. Например:
//
// array = [9,3,6,10] --> ранги = [2,4,3,1]
//
// array = [3,3,3,3,3,5,1] --> ранги = [2,2,2,2,2,1,7]
//
// поскольку существует одно значение, занимающее первое место, пятеро участников, претендующих на второе место, и один, занимающий седьмое место.





function ranks(scores) {
    const sorted = [...scores].sort((a, b) => b - a);

    const rankMap = new Map();
    let currentRank = 1;

    for (let i = 0; i < sorted.length; i++) {
        if (i === 0 || sorted[i] !== sorted[i-1]) {
            currentRank = i + 1;
        }
        rankMap.set(sorted[i], currentRank);
    }

    return scores.map(score => rankMap.get(score));
}